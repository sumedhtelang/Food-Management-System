    private void processRequestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processRequestBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProcess.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first from the table to view details", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            RequestGovernmentRequest request = (RequestGovernmentRequest) tblProcess.getValueAt(selectedRow, 5);
            if (request.getCurrentStatus().equalsIgnoreCase("Sent to Treasurer")) {
                JOptionPane.showMessageDialog(null, "Please assign selected request first");
                return;
            }
            if (request.getCurrentStatus().equalsIgnoreCase("Accepted")) {
                JOptionPane.showMessageDialog(null, "Request already completed");
                return;
            }
            if(!userAccount.equals(request.getReceiver())){
             JOptionPane.showMessageDialog(null, "Not Authorized");
                return;
            }
            if (!userAccount.getEmployee().equals(request.getReceiver().getEmployee())) {
                JOptionPane.showMessageDialog(null, "Request assigned to other Officer", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (request.getCurrentStatus().equals("Rejected")) {
                JOptionPane.showMessageDialog(null, "Cannot process a Rejected Request", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            //request.setStatus("Processing");
            HeadRequestJPanel treasurerProcessWorkRequestJPanel = new HeadRequestJPanel(userProcessContainer, request);
            userProcessContainer.add("TreasurerProcessWorkRequestJPanel", treasurerProcessWorkRequestJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_processRequestBtnActionPerformed

    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProcess.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first from the table to view details", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            WorkRequest request = (RequestGovernmentRequest) tblProcess.getValueAt(selectedRow, 5);
            if (request.getCurrentStatus().equals("Sent to Treasurer")) {
                request.setReceiver(userAccount);
                request.setCurrentStatus("Pending on " + request.getReceiver().getEmployee().getName());
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Can't assign this work request, as the work request is in " + request.getCurrentStatus() + " status");
            }
        }
        
    }//GEN-LAST:event_btnAssignActionPerformed


