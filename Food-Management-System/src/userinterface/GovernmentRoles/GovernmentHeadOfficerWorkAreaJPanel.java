    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblProcess.getModel();
        model.setRowCount(0);

        for (WorkRequest request : governmentHeadOrganization.getWorkQueue().getWorkRequestList()) {
            String status = request.getCurrentStatus();
            Object[] row = new Object[6];
            row[0] = request.getProvider().getEmployee().getName();
            if (status.equalsIgnoreCase("Sent") || status.equalsIgnoreCase("Sent")) {
                row[1] = null;
            } else {
                row[1] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            }
            //row[1] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[2] = status;
            row[3] = ((RequestGovernmentRequest) request).getRequestAmount();
            row[4] = ((RequestGovernmentRequest) request).getMessage();
            row[5] = request;

            model.addRow(row);
        }
    }
    
